    # 小数
    ("0.5",      "零点五"),
    ("3.14",     "三点一四"),
    ("10.5",     "十点五"),
    ("100.01",   "一百点零一"),
    # 日期时间
    ("2026年4月13日",  "二零二六年四月十三日"),
    ("2026-04-13",     "二零二六年零四月十三日"),
    ("10月11日",       "十月十一日"),
    ("上午10:30",      "上午十点三十分"),
    ("下午3:05",       "下午三点零五分"),
