"""Run @语言学之父 Japanese batch-1 test cases."""
import sys
sys.path.insert(0, "/home/babysor00/tts-normalizer")
from tts_normalizer import Normalizer

ja = Normalizer(lang="ja")

cases = [
    # 整数——日語固有規則
    ("1",               "一"),
    ("10",              "十"),
    ("100",             "百"),
    ("1000",            "千"),
    ("10000",           "一万"),
    ("100000000",       "一億"),
    ("12345",           "一万二千三百四十五"),
    ("100000",          "十万"),
    ("10000000",        "千万"),
    ("123456789",       "一億二千三百四十五万六千七百八十九"),
    # 年号——逐位読み
    ("2026年",          "二〇二六年"),
    ("1999年",          "一九九九年"),
    ("2000年",          "二〇〇〇年"),
    ("100年前",         "百年前"),
    # 日付・時刻
    ("4月13日",         "四月十三日"),
    ("2026年4月13日",   "二〇二六年四月十三日"),
    ("10時30分",        "十時三十分"),
    ("3時5分",          "三時五分"),
    ("午後3時05分",     "午後三時五分"),
    # 通貨
    ("¥1000",           "千円"),
    ("¥10000",          "一万円"),
    ("¥1500",           "千五百円"),
    ("¥100",            "百円"),
    ("$100",            "百ドル"),
    ("€50",             "五十ユーロ"),
    # パーセント・小数・負数
    ("50%",             "五十パーセント"),
    ("100%",            "百パーセント"),
    ("3.14",            "三点一四"),
    ("0.5",             "零点五"),
    ("-5",              "マイナス五"),
    ("-3.14",           "マイナス三点一四"),
    # 単位
    ("50kg",            "五十キログラム"),
    ("10cm",            "十センチメートル"),
    ("100km",           "百キロメートル"),
    ("25°C",            "二十五度"),
    ("-5°C",            "マイナス五度"),
    # 電話番号
    ("03-1234-5678",    "〇三一二三四五六七八"),
    ("090-1234-5678",   "〇九〇一二三四五六七八"),
    # 序数・版本号
    ("第1位",           "第一位"),
    ("第3章",           "第三章"),
    ("v2.3.1",          "v二点三点一"),
    # 歧義ケース
    ("1000円のランチ",  "千円のランチ"),
    ("1000人の観客",    "千人の観客"),
    ("一万円札",        "一万円札"),
]

passed = failed = 0
failures = []
for inp, expected in cases:
    got = ja.normalize(inp)
    if got == expected:
        passed += 1
    else:
        failed += 1
        failures.append((inp, expected, got))

print(f"\n{'='*60}")
print(f"PASSED: {passed}/{passed+failed}")
print(f"FAILED: {failed}/{passed+failed}")
if failures:
    print("\nFailed cases:")
    for inp, exp, got in failures:
        print(f"  Input:    {inp!r}")
        print(f"  Expected: {exp!r}")
        print(f"  Got:      {got!r}")
        print()
